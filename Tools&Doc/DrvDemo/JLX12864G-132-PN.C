/*写指令到LCD模块*/
void transfer_command(int data1)   
{
	char i;
	cs1=0;
	rs=0;
	for(i=0;i<8;i++)
	{
		sclk=0;
		if(data1&0x80) sid=1;
		else sid=0;
		sclk=1;
		delay_us(1);
		data1=data1<<=1;
	}
}

/*写数据到LCD模块*/
void transfer_data(int data1)
{
	char i;
	cs1=0;
	rs=1;
	for(i=0;i<8;i++)
	{
		sclk=0;
		if(data1&0x80) sid=1;
		else sid=0;
		sclk=1;
		delay_us(1);
		data1=data1<<=1;
	}
}

/*延时*/
void delay(int i)               
{
 int j,k;
 for(j=0;j<i;j++)
 for(k=0;k<110;k++);
}

/*延时*/
void delay_us(int i)               
{
 int j,k;
 for(j=0;j<i;j++)
 for(k=0;k<10;k++);
}

